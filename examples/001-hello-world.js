// We can change the colors on the
// screen using functions. Functions
// are how we tell the computer what
// to do.

// Here we are calling the "clear"
// function, which makes the whole
// screen one color.

// Try changing the color from
// "magenta" to "white".
// Press the [ ‚Ė∂ Run ] button in the
// top left corner to see the program
// in action.
clear({ color: "magenta" });

// Lets examine the syntax of this
// line of code. Syntax refers to the
// structure of letters and symbols
// that make up the code.

// I will use arrows (^^^) to point
// to different parts of the line.

// We start with the name of the
// function.
// clear({ color: "magenta"});
// ^^^^^

// Then we have parentheses which
// tell us we would like to execute
// the function. They also provide a
// space to specify information about
// how we want it to be done.
// clear({ color: "magenta"});
//      ^                   ^

// Next the curly brackets specify an
// object. An object contains
// information that can be read by the
// function definition. I'll show you
// how to do this later.
// clear({ color: "magenta"});
//       ^                 ^

// The object contains keys and
// values. Keys give names to data
// and values specify the data itself.
// The "clear" function expects an
// object that defines the "color"
// key with a value of a specific
// color.

// So provide the key "color" in our
// object.
// clear({ color: "magenta"});
//         ^^^^^

// Then we use the colon to separate
// the key from the value.
// clear({ color: "magenta"});
//              ^

// The value is a string, which is
// enclosed in quotes. Here we can
// say the name of a color.
// clear({ color: "magenta"});
//                ^^^^^^^^^

// Finally we end the line with a
// semicolon.
// clear({ color: "magenta"});
//                           ^

